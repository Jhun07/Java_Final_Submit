    public void update() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            //Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/employeevic","root","");  
            java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/javaInventory", "root", "");
            Statement stmt = con.createStatement();

            String query = "UPDATE `products` SET product_title='" + this.upp.getText() + "',category='" + this.upc.getSelectedItem() + "',in_stock='" + this.upi.getText() + "', buying_price='" + this.upbp.getText() + "', selling_price='" + this.upsp.getText() + "' WHERE id='" + this.uppid.getText() + "'";
            stmt.execute(query);
            con.close();
            new ManageProduct().setVisible(true);
            this.setVisible(false);
            JOptionPane.showMessageDialog(null, "Product is successfully updated.", "Alert", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            System.out.println(e);
        }

    }

